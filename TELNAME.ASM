CGROUP	GROUP	CODE_SEG, DATA_SEG
	ASSUME	CS:CGROUP, DS:CGROUP, ES:CGROUP
	
CODE_SEG	SEGMENT	'CODE'
	ORG	100H
MAIN	PROC	NEAR
	MOV	AH,9
	MOV	DX,OFFSET CGROUP:CLS
	INT	21H
	MOV	AX,1301H
	MOV	BX,7
	XOR	DX,DX
	MOV	CX,22
	MOV	BP,OFFSET CGROUP:MESSAGE
	INT	10H
	MOV	AH,0AH
	MOV	DX,OFFSET CGROUP:CHARS
	INT	21H
	MOV	AH,2
	MOV	DX,300H
	INT	10H
	MOV	SI,OFFSET CGROUP:CHARS+2
	CLD
	MOV	CL,CHARS+1
	JCXZ	EN
	MOV	BX,OFFSET CGROUP:TABLE
AGAIN:	LODSB
	CMP	AL,'y'
	JA	J_PRN
	CMP	AL,'A'
	JB	J_PRN
	CMP	AL,'Z'
	JB	CHANGE
	CMP	AL,'a'
	JAE	CONV
	JMP	J_PRN
CHANGE:	OR	AL,32
CONV:	SUB	AL,'a'
	XLAT
J_PRN:	INT	29H
	LOOP	AGAIN
EN:	XOR	BH,BH
	MOV	DX,700H
	INT	10H			;MOVE DOWN A FEW
	INT	20H			;END
MAIN	ENDP
CODE_SEG	ENDS

DATA_SEG	SEGMENT	'DATA'
	MESSAGE	DB	'Enter telephone name: '
	CHARS	DB	80, 81 DUP(?)
	TABLE	DB	'2','2','2','3','3','3','4','4','4','5','5'
		DB	'5','6','6','6','7',' ','7','7','8','8','8'
		DB	'9','9','9'
	CLS	DB	27,'[2J$'
DATA_SEG	ENDS


	END	MAIN